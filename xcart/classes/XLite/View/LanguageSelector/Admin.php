namespace XLite\View\LanguageSelector;

/**
 * Language selector (admin)
 */
class Admin extends \XLite\View\LanguageSelector\ALanguageSelector
{
    /**
     * Check if widget is visible
     *
     * @return boolean
     */
    protected function isVisible()
    {
        return parent::isVisible()
            && \XLite\Core\Auth::getInstance()->isAdmin();
    }
}
