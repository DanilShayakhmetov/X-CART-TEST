namespace XLite\Module\XPay\XPaymentsCloud\View;

/**
 * Subscriptions page view
 *
 * @ListChild (list="admin.center", zone="admin")
 */
class Subscription extends \XLite\View\AView
{
    /**
     * Return list of allowed targets
     *
     * @return array
     */
    public static function getAllowedTargets()
    {
        $list = parent::getAllowedTargets();
        $list[] = 'xpayments_subscriptions';

        return $list;
    }

    /**
     * Return widget default template
     *
     * @return string
     */
    protected function getDefaultTemplate()
    {
        return 'modules/XPay/XPaymentsCloud/subscription/body.twig';
    }

    /**
     * Check if widget is visible
     *
     * @return boolean
     */
    protected function isVisible()
    {
        return parent::isVisible()
            && !\XLite\Core\Request::getInstance()->subscription_id
            && !\XLite\Core\Request::getInstance()->profile_id;
    }

}
